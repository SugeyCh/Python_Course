# BUCLE WHILE
# Permite repetir una sentencia varias veces siempre y cuando la condición se cumpla (sea True)
contador = 0
while contador < 10:
  print(contador)
  contador+=1
  
# Se pone: contador+=1, para que aumente su número, ya que si se deja solamente con el print, 
# el código se seguirá ejecutando de manera infinita.